// hostInterfaces declares the read-only host APIs hooks receive on
// RenderHookContext: Std (file/env reads), Os (filesystem inspection),
// and Fetch (HTTP client). All write/exec/mutate bindings from QuickJS's
// std/os modules are stripped at runtime by the host-namespace lockdown
// in pkg/hook — these declarations are the entire surface hooks can see.
//
// Paths resolve against the veil project root (the directory housing
// veil.json) — the render pipeline chdir's into root before invoking hooks,
// so plain relative paths like "data.txt" or "config/env.yaml" are what
// hooks should use. `ctx.root` is the absolute root path, exposed for
// logging or display.
const hostInterfaces = `export interface Std {
  /** Read the entire file as a string, or null on failure. The path is
   *  resolved relative to the veil project root — the first ancestor
   *  directory (walking up from where ` + "`veil`" + ` was invoked) that
   *  contains a veil.json. */
  loadFile(path: string): string | null;
  /** Read the value of an environment variable, or undefined if unset. */
  getenv(name: string): string | undefined;
}

/** File metadata returned by os.stat / os.lstat. All values are numbers. */
export interface Stats {
  dev: number;
  ino: number;
  mode: number;
  nlink: number;
  uid: number;
  gid: number;
  rdev: number;
  size: number;
  blocks: number;
  atime: number;
  mtime: number;
  ctime: number;
}

export interface Os {
  /** List directory entries. Returns [names, error] — error is 0 on success. */
  readdir(path: string): [string[], number];
  /** File metadata, following symlinks. Returns [stats, error]. */
  stat(path: string): [Stats, number];
  /** File metadata, *not* following symlinks. Returns [stats, error]. */
  lstat(path: string): [Stats, number];
  /** Resolve to a canonical absolute path. Returns [path, error]. */
  realpath(path: string): [string, number];
  /** Read a symbolic link's target. Returns [target, error]. */
  readlink(path: string): [string, number];
  /** Process working directory — returns [cwd, error]. */
  getcwd(): [string, number];
  /** Platform identifier (e.g. "linux", "darwin"). */
  platform: string;
}

export interface FetchInit {
  method?: string;
  headers?: Record<string, string>;
  body?: string;
}

export interface FetchHeaders {
  get(name: string): string | null;
  has(name: string): boolean;
  forEach(fn: (value: string, key: string) => void): void;
}

export interface FetchResponse {
  status: number;
  statusText: string;
  ok: boolean;
  url: string;
  headers: FetchHeaders;
  text(): Promise<string>;
  json(): Promise<unknown>;
}

/** Web Fetch-style client. Strings only for body; no streaming, no
 *  AbortController. See SPEC.md for the full list of intentional gaps. */
export type Fetch = (
  input: string | (FetchInit & { url: string }),
  init?: FetchInit,
) => Promise<FetchResponse>;
`

const fileInterface = `export interface File {
  /** Current contents. */
  getContent(): string;
  /** Overwrite the contents. */
  setContent(content: string): void;
  /** Current destination path, relative to the render output directory. */
  getPath(): string;
  /** Override the destination path. Identity (the FS key) is unchanged, so
   *  downstream hooks still find this file via the same typed accessor. */
  setOutputPath(path: string): void;
  /** True when this file has been tombstoned via setDeleted(true) or
   *  fs.delete(). Downstream hooks still observe the entry; the final
   *  writer skips it. */
  isDeleted(): boolean;
  /** Tombstone (true) or restore (false) this entry. Identity is
   *  preserved either way — downstream hooks still find it via the same
   *  typed accessor. */
  setDeleted(deleted: boolean): void;
}
`

const hookInterface = `export interface RenderHook {
  /**
   * Called during ` + "`veil render`" + ` to transform the filesystem state for one
   * resource. Receives a RenderHookContext (resource, vars, root, std, os,
   * fetch) and the current FS; return the (optionally mutated) FS to flow
   * it into the next hook in the pipeline.
   *
   * Read declared sources via typed accessors (e.g. ` + "`fs.getSourcesDeploymentYaml()`" + `).
   * Modify a file with ` + "`setContent`, `setOutputPath`, or `setDeleted`" + `. Create
   * new files with ` + "`fs.add(path, content)`" + `. Soft-delete with ` + "`fs.delete(path)`" + ` —
   * tombstoned files persist so downstream hooks can observe them via
   * ` + "`file.isDeleted()`" + `, and are skipped at final write.
   *
   * Returning nothing passes the FS through unchanged. ` + "`throw new Error(...)`" + `
   * to abort the render for this resource. May be ` + "`async`" + ` — the host awaits
   * the returned Promise transparently before handing the result off.
   */
  render(ctx: RenderHookContext, fs: FS): FS | void | Promise<FS | void>;
}
`
