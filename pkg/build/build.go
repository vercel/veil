// Package build contains the TypeScript-generation machinery used by
// `veil build` and the scaffolding commands: the hardcoded Hook / FS /
// File / Std / Os type declarations, per-kind veil-types.ts assembly,
// composite resource JSON-schema emission, and the path→accessor-name
// algorithm that keeps build-time types and runtime FS methods in sync.
//
// Callers in pkg/commands treat this package as a pure string/file
// producer — no CLI flags or printers leak in.
package build

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strconv"
	"strings"
	"unicode"

	"github.com/goccy/go-json"

	veilv1 "github.com/vercel/veil/api/go/veil/v1"
	"github.com/vercel/veil/pkg/config"
	"github.com/vercel/veil/pkg/typegen"
)

// VeilTypes renders the per-kind veil-types.ts file containing the kind's
// Spec interface, RegistryVariables, host API types (Std, Os, Fetch …),
// File, FS (with one generated File-returning accessor per declared
// source), RenderHookContext, and Hook.
func VeilTypes(k config.Kind, variables map[string]*veilv1.Variable) (string, error) {
	specInterface, err := specInterface(k)
	if err != nil {
		return "", err
	}
	specTypeName := PascalCase(k.Name) + "Spec"

	fsIface, err := fsInterface(k)
	if err != nil {
		return "", err
	}

	varsIface, err := variablesInterface(variables)
	if err != nil {
		return "", err
	}

	var b strings.Builder
	b.WriteString("// Generated by veil build — do not edit.\n\n")
	b.WriteString(specInterface)
	b.WriteString("\n")
	b.WriteString(varsIface)
	b.WriteString("\n")
	b.WriteString(hostInterfaces)
	b.WriteString("\n")
	b.WriteString(fileInterface)
	b.WriteString("\n")
	b.WriteString(fsIface)
	b.WriteString("\n")
	fmt.Fprintf(&b, renderHookContextTemplate, specTypeName)
	b.WriteString(hookInterface)

	return b.String(), nil
}

const renderHookContextTemplate = `export interface Override {
  /** Path of the source file in the kind being replaced. */
  source: string;
  /** Local path to the override file. */
  path: string;
}

export interface Metadata {
  /** Name of the kind this resource instantiates. */
  kind: string;
  /** Name of this resource. */
  name: string;
  /** Source files ejected for local override. */
  overrides?: Override[];
}

export interface Resource<Spec> {
  /** Standard metadata common to every resource. */
  metadata: Metadata;
  /** Resource-specific data, validated against the kind's schema and
   *  with any matching overlays already merged in. */
  spec: Spec;
}

export interface RenderHookContext {
  /** The resource being rendered — full metadata + merged spec. */
  resource: Resource<%s>;
  /** Resolved input variables (from --var, env, or defaults). */
  vars: RegistryVariables;
  /** Absolute path of the veil project root — the directory housing
   *  veil.json. Provided for display / logging; std and os already
   *  resolve paths relative to this root. */
  root: string;
  /** Read-only file / env access. Paths resolve relative to the project
   *  root; use plain relative strings like "data.txt". */
  std: Std;
  /** Read-only filesystem inspection — list directories, stat, resolve
   *  symlinks. Paths resolve relative to the project root. */
  os: Os;
  /** Web Fetch-style HTTP client. Honors the host's allowlist, timeout,
   *  and response-size cap. */
  fetch: Fetch;
}

`

// HookTemplate renders the default scaffolded hook .ts. It imports FS,
// RenderHook, and RenderHookContext from the sibling veil-types.ts
// emitted by `veil build` and exports a RenderHook with a placeholder
// `render` method.
func HookTemplate(hookName string) string {
	var b strings.Builder

	b.WriteString("// Generated by veil — edit as needed.\n\n")
	b.WriteString("import type { FS, RenderHook, RenderHookContext } from './veil-types';\n\n")

	fnName := CamelCase(hookName)
	fmt.Fprintf(&b, "const %s: RenderHook = {\n", fnName)
	b.WriteString("  render(ctx: RenderHookContext, fs: FS): FS {\n")
	b.WriteString("    // TODO: implement\n")
	b.WriteString("    return fs;\n")
	b.WriteString("  },\n")
	b.WriteString("};\n\n")
	fmt.Fprintf(&b, "export default %s;\n", fnName)

	return b.String()
}

// ResourceSchema builds the composite JSON Schema for a kind (metadata +
// spec envelope) and writes it to outPath.
func ResourceSchema(k config.Kind, metadataSchema map[string]any, outPath string) error {
	specSchema, err := LoadSpecSchema(k)
	if err != nil {
		return err
	}

	delete(specSchema, "$schema")
	delete(specSchema, "$id")

	schema := map[string]any{
		"$schema":              "https://json-schema.org/draft/2020-12/schema",
		"type":                 "object",
		"additionalProperties": false,
		"title":                k.Name,
		"description":          fmt.Sprintf("Resource schema for kind %q.", k.Name),
		"required":             []string{"metadata", "spec"},
		"properties": map[string]any{
			"metadata": metadataSchema,
			"spec":     specSchema,
		},
	}

	data, err := json.MarshalIndent(schema, "", "  ")
	if err != nil {
		return fmt.Errorf("marshalling schema: %w", err)
	}

	return os.WriteFile(outPath, data, 0644)
}

// LoadSpecSchema reads and parses the kind's schema file. If no schema
// is defined, returns a permissive object schema.
func LoadSpecSchema(k config.Kind) (map[string]any, error) {
	if k.Schema == "" {
		return map[string]any{
			"type":        "object",
			"description": "Resource-specific configuration.",
		}, nil
	}

	schemaPath := k.Schema
	if !filepath.IsAbs(schemaPath) {
		schemaPath = filepath.Join(k.Dir, schemaPath)
	}

	data, err := os.ReadFile(schemaPath)
	if err != nil {
		return nil, fmt.Errorf("reading schema %s: %w", schemaPath, err)
	}

	var spec map[string]any
	if err := json.Unmarshal(data, &spec); err != nil {
		return nil, fmt.Errorf("parsing schema %s: %w", schemaPath, err)
	}
	return spec, nil
}

// MethodSuffixForPath converts a declared source path into the PascalCase
// suffix used for its typed FS accessor. `./sources/my-file.yaml` →
// `SourcesMyFileYaml`. The companion JS implementation
// (__veilMethodSuffix in pkg/hook) must stay in lockstep — any change
// here needs to be mirrored there, or build-time types and runtime
// methods will drift.
func MethodSuffixForPath(p string) string {
	p = strings.TrimPrefix(p, "./")
	var b strings.Builder
	capitalize := true
	for _, c := range p {
		switch c {
		case '/', '.', '-', '_', ' ':
			capitalize = true
			continue
		}
		if capitalize {
			b.WriteRune(unicode.ToUpper(c))
			capitalize = false
		} else {
			b.WriteRune(c)
		}
	}
	return b.String()
}

// PascalCase converts a kebab/snake name to PascalCase.
func PascalCase(s string) string {
	var b strings.Builder
	upper := true
	for _, c := range s {
		if c == '-' || c == '_' {
			upper = true
			continue
		}
		if upper {
			b.WriteRune(rune(strings.ToUpper(string(c))[0]))
			upper = false
		} else {
			b.WriteRune(c)
		}
	}
	return b.String()
}

// CamelCase converts a kebab/snake name to camelCase.
func CamelCase(s string) string {
	pascal := PascalCase(s)
	if pascal == "" {
		return ""
	}
	return strings.ToLower(pascal[:1]) + pascal[1:]
}

// ---- internal helpers --------------------------------------------------

// specInterface produces a TypeScript interface from the kind's spec
// schema using the shared typegen package.
func specInterface(k config.Kind) (string, error) {
	specSchemaRaw, err := LoadSpecSchema(k)
	if err != nil {
		return "", err
	}
	data, err := json.Marshal(specSchemaRaw)
	if err != nil {
		return "", err
	}
	var schema typegen.Schema
	if err := json.Unmarshal(data, &schema); err != nil {
		return "", err
	}
	return typegen.GenerateInterface(PascalCase(k.Name)+"Spec", schema), nil
}

// fsInterface emits the FS interface for a kind. One typed accessor per
// declared source plus generic escape hatches. Accessor-name collisions
// surface as errors at build time.
func fsInterface(k config.Kind) (string, error) {
	sources := append([]string(nil), k.Sources...)
	sort.Strings(sources)

	type accessor struct {
		method string
		source string
	}
	accessors := make([]accessor, 0, len(sources))
	seen := map[string]string{}
	for _, src := range sources {
		suffix := MethodSuffixForPath(src)
		if suffix == "" {
			continue
		}
		method := "get" + suffix
		if prev, ok := seen[method]; ok {
			return "", fmt.Errorf("sources %q and %q both map to accessor %s; rename one to disambiguate", prev, src, method)
		}
		seen[method] = src
		accessors = append(accessors, accessor{method: method, source: src})
	}

	var b strings.Builder
	b.WriteString("export interface FS {\n")
	for _, a := range accessors {
		b.WriteString(fmt.Sprintf("  /** Handle for the declared source %q. */\n", a.source))
		b.WriteString(fmt.Sprintf("  %s(): File;\n", a.method))
	}
	if len(accessors) > 0 {
		b.WriteString("\n")
	}
	b.WriteString("  /** Look up a file handle by its path. Returns undefined if absent. */\n")
	b.WriteString("  get(path: string): File | undefined;\n")
	b.WriteString("  /** Every file handle currently present (including tombstoned ones —\n")
	b.WriteString("   *  filter via `file.isDeleted()` if you only want live files). */\n")
	b.WriteString("  getAll(): File[];\n")
	b.WriteString("  /** Create a new file at the given path. Errors if a file with that path already exists. */\n")
	b.WriteString("  add(path: string, content: string): File;\n")
	b.WriteString("  /** Tombstone the file at the given path (soft delete — downstream hooks still see it). */\n")
	b.WriteString("  delete(path: string): void;\n")
	b.WriteString("  /** All paths currently present. */\n")
	b.WriteString("  keys(): string[];\n")
	b.WriteString("}\n")

	return b.String(), nil
}

// variablesInterface emits the RegistryVariables interface. Keys are
// sorted for deterministic output. Descriptions → JSDoc. Enums → union
// literal types.
func variablesInterface(variables map[string]*veilv1.Variable) (string, error) {
	var b strings.Builder
	b.WriteString("export interface RegistryVariables {\n")
	names := make([]string, 0, len(variables))
	for name := range variables {
		names = append(names, name)
	}
	sort.Strings(names)
	for _, name := range names {
		v := variables[name]
		if desc := v.GetDescription(); desc != "" {
			b.WriteString(tsDocComment(desc, "  "))
		}
		tsType, err := tsTypeForVariable(v)
		if err != nil {
			return "", fmt.Errorf("variable %q: %w", name, err)
		}
		b.WriteString(fmt.Sprintf("  %s: %s;\n", name, tsType))
	}
	b.WriteString("}\n")
	return b.String(), nil
}

// tsDocComment renders desc as a JSDoc comment with the given indent.
// Single line → `/** desc */`. Multi-line → a block with `*` gutter.
// `*/` sequences are escaped so they don't close the comment.
func tsDocComment(desc, indent string) string {
	desc = strings.ReplaceAll(desc, "*/", "*\\/")
	lines := strings.Split(desc, "\n")
	if len(lines) == 1 {
		return fmt.Sprintf("%s/** %s */\n", indent, lines[0])
	}
	var b strings.Builder
	b.WriteString(indent)
	b.WriteString("/**\n")
	for _, line := range lines {
		b.WriteString(indent)
		b.WriteString(" * ")
		b.WriteString(line)
		b.WriteByte('\n')
	}
	b.WriteString(indent)
	b.WriteString(" */\n")
	return b.String()
}

// tsTypeForVariable returns the TS type expression for a variable,
// honoring any declared enum (union literal) over the broad scalar type.
func tsTypeForVariable(v *veilv1.Variable) (string, error) {
	if len(v.Enum) > 0 {
		parsed, err := config.ParsedEnum(v)
		if err != nil {
			return "", err
		}
		parts := make([]string, 0, len(parsed))
		for _, val := range parsed {
			switch x := val.(type) {
			case string:
				parts = append(parts, strconv.Quote(x))
			case float64:
				parts = append(parts, formatNumberLiteral(x))
			default:
				return "", fmt.Errorf("unsupported enum value type %T", val)
			}
		}
		return strings.Join(parts, " | "), nil
	}
	switch v.Type {
	case veilv1.VariableType_string:
		return "string", nil
	case veilv1.VariableType_number:
		return "number", nil
	case veilv1.VariableType_bool:
		return "boolean", nil
	default:
		return "unknown", nil
	}
}

// formatNumberLiteral emits a minimal TS numeric literal — 3 stays "3",
// 3.14 stays "3.14".
func formatNumberLiteral(f float64) string {
	if f == float64(int64(f)) {
		return strconv.FormatInt(int64(f), 10)
	}
	return strconv.FormatFloat(f, 'g', -1, 64)
}

